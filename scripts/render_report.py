"""Render a report from its draft(s) into self-contained HTML file(s).

Inputs (all under <site>/<slug>/):
    meta.yaml           — title, subtitle, slug, lang, langs, date, tags, status
                          Bilingual reports use suffixed keys for the secondary
                          language (e.g. title_ko, subtitle_ko).
    draft.md            — primary-language markdown with [^sNN] footnote refs
    draft.<code>.md     — additional language drafts for each code in `langs`
                          other than the primary `lang`
    working/sources.jsonl — shared across all languages

Outputs:
    <site>/<slug>/index.html          — primary language (meta.lang)
    <site>/<slug>/<code>/index.html   — each alternate language in meta.langs
"""
